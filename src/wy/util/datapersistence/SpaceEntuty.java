package wy.util.datapersistence;

import javax.persistence.Entity;

@Entity
public class SpaceEntuty {
	
	private int objid;			//对象id
	private int id;			//id
	private String geometry;	//坐标信息--例;"{"type":"Point","coordinates":[120.428628398327,36.1483420840128]}"
	
	public SpaceEntuty(){}
	public SpaceEntuty(int objid, int id, String geometry) {
		super();
		this.objid = objid;
		this.id = id;
		this.geometry = geometry;
	}
	
	public int getObjid() {
		return objid;
	}
	public void setObjid(int objid) {
		this.objid = objid;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getGeometry() {
		return geometry;
	}
	public void setGeometry(String geometry) {
		this.geometry = geometry;
	}
}
