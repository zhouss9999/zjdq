package wy.qingdao_atmosphere.countrysitedata.domain;

public class CpDir {
	private Integer id;
	
	private String dirname;
	
	private String dirlevel;
	
	private String higherlevelid;
	
	private String lastlevel;
	
	private String fieldid;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getDirname() {
		return dirname;
	}

	public void setDirname(String dirname) {
		this.dirname = dirname;
	}

	public String getDirlevel() {
		return dirlevel;
	}

	public void setDirlevel(String dirlevel) {
		this.dirlevel = dirlevel;
	}

	public String getHigherlevelid() {
		return higherlevelid;
	}

	public void setHigherlevelid(String higherlevelid) {
		this.higherlevelid = higherlevelid;
	}

	public String getLastlevel() {
		return lastlevel;
	}

	public void setLastlevel(String lastlevel) {
		this.lastlevel = lastlevel;
	}

	public String getFieldid() {
		return fieldid;
	}

	public void setFieldid(String fieldid) {
		this.fieldid = fieldid;
	}
	
	
	
	

}
