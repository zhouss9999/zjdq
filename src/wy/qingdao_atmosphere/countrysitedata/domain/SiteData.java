package wy.qingdao_atmosphere.countrysitedata.domain;

import javax.persistence.Entity;

@Entity
public class SiteData {
	
	private long dataid;
	
	private String dataguid;
	
	private String datavalue;
	
	private String collecttime;

	public long getDataid() {
		return dataid;
	}

	public void setDataid(long dataid) {
		this.dataid = dataid;
	}

	public String getDataguid() {
		return dataguid;
	}

	public void setDataguid(String dataguid) {
		this.dataguid = dataguid;
	}

	public String getDatavalue() {
		return datavalue;
	}

	public void setDatavalue(String datavalue) {
		this.datavalue = datavalue;
	}

	public String getCollecttime() {
		return collecttime;
	}

	public void setCollecttime(String collecttime) {
		this.collecttime = collecttime;
	}

	@Override
	public String toString() {
		return "SiteData [dataid=" + dataid + ", dataguid=" + dataguid
				+ ", datavalue=" + datavalue + ", collecttime=" + collecttime
				+ "]";
	}
	
	
}
