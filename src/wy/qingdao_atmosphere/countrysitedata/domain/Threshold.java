package wy.qingdao_atmosphere.countrysitedata.domain;

import javax.persistence.Entity;

@Entity
public class Threshold {
	private String thrid;			//报警id
	private String objid;			//对象id
	private String collecttime;		//监测时间
	private String paramid;			//参数id
	private String value;			//监测值
	private String thresholdval;	//阀值
	private String describe;		//描述
	private String thrtype;			//报警类型
	
	public Threshold() {
		super();
	}
	public Threshold(String objid, String collecttime,
			String paramid, String value, String thresholdval, String describe,String thrtype) {
		super();
		this.objid = objid;
		this.collecttime = collecttime;
		this.paramid = paramid;
		this.value = value;
		this.thresholdval = thresholdval;
		this.describe = describe;
		this.thrtype = thrtype;
	}
	
	public String getThrid() {
		return thrid;
	}
	public void setThrid(String thrid) {
		this.thrid = thrid;
	}
	public String getObjid() {
		return objid;
	}
	public void setObjid(String objid) {
		this.objid = objid;
	}
	public String getCollecttime() {
		return collecttime;
	}
	public void setCollecttime(String collecttime) {
		this.collecttime = collecttime;
	}
	public String getParamid() {
		return paramid;
	}
	public void setParamid(String paramid) {
		this.paramid = paramid;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getThresholdval() {
		return thresholdval;
	}
	public void setThresholdval(String thresholdval) {
		this.thresholdval = thresholdval;
	}
	public String getDescribe() {
		return describe;
	}
	public void setDescribe(String describe) {
		this.describe = describe;
	}
	public String getThrtype() {
		return thrtype;
	}
	public void setThrtype(String thrtype) {
		this.thrtype = thrtype;
	}
	
}
