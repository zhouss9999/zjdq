package wy.qingdao_atmosphere.datacenter.domain;

import javax.persistence.Entity;

@Entity
public class Objtype {
	private int objtypeid;// 对象类型ID
	private String objtypename;//对象类型名称
	private String spacetypename;//空间类型名称
	private String spacelayername;//空间图层名称
	private int isshow;//是否显示
	private String ismove;// 是否移动
	private String isused;
	private String remark;
	public int getObjtypeid() {
		return objtypeid;
	}
	public void setObjtypeid(int objtypeid) {
		this.objtypeid = objtypeid;
	}
	public String getObjtypename() {
		return objtypename;
	}
	public void setObjtypename(String objtypename) {
		this.objtypename = objtypename;
	}
	public String getSpacetypename() {
		return spacetypename;
	}
	public void setSpacetypename(String spacetypename) {
		this.spacetypename = spacetypename;
	}
	public String getSpacelayername() {
		return spacelayername;
	}
	public void setSpacelayername(String spacelayername) {
		this.spacelayername = spacelayername;
	}
	public int getIsshow() {
		return isshow;
	}
	public void setIsshow(int isshow) {
		this.isshow = isshow;
	}
	public String getIsmove() {
		return ismove;
	}
	public void setIsmove(String ismove) {
		this.ismove = ismove;
	}
	public String getIsused() {
		return isused;
	}
	public void setIsused(String isused) {
		this.isused = isused;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}
