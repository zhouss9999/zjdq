package wy.qingdao_atmosphere.impExcelData.domain;

import javax.persistence.Entity;

@Entity
public class ObjInfoDatas {
	long objid;
	int objtype;
	String objname;
	String objnum;
	String objremark;
	int isused;
	String provice;
	String city;
	String country;
	
	public ObjInfoDatas(){
		
	}
	
	public ObjInfoDatas(long objid, int objtype, String objname, String objnum,
			String objremark, int isused, String provice, String city,
			String country) {
		super();
		this.objid = objid;
		this.objtype = objtype;
		this.objname = objname;
		this.objnum = objnum;
		this.objremark = objremark;
		this.isused = isused;
		this.provice = provice;
		this.city = city;
		this.country = country;
	}
	
	public long getObjid() {
		return objid;
	}
	public void setObjid(long objid) {
		this.objid = objid;
	}
	public int getObjtype() {
		return objtype;
	}
	public void setObjtype(int objtype) {
		this.objtype = objtype;
	}
	public String getObjname() {
		return objname;
	}
	public void setObjname(String objname) {
		this.objname = objname;
	}
	public String getObjnum() {
		return objnum;
	}
	public void setObjnum(String objnum) {
		this.objnum = objnum;
	}
	public String getObjremark() {
		return objremark;
	}
	public void setObjremark(String objremark) {
		this.objremark = objremark;
	}
	public int getIsused() {
		return isused;
	}
	public void setIsused(int isused) {
		this.isused = isused;
	}
	public String getProvice() {
		return provice;
	}
	public void setProvice(String provice) {
		this.provice = provice;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	
	
}
